export const filterGuaritaData = (data, { search, evento, status }) => {
  return data.filter(person => {
    const matchesSearch = !search || 
      person.nome.toLowerCase().includes(search.toLowerCase()) ||
      person.cpf.replace(/\D/g, '').includes(search.replace(/\D/g, '')) ||
      person.fornecedor.toLowerCase().includes(search.toLowerCase()) ||
      person.evento.toLowerCase().includes(search.toLowerCase());
      
    const matchesEvento = !evento || evento === 'todos' || person.evento === evento;
    
    const matchesStatus = !status || status === 'todos' || person.status === status;

    return matchesSearch && matchesEvento && matchesStatus;
  });
};

export const maskCpf = (cpf) => {
  if (!cpf) return '';
  return `***.***.***-${cpf.slice(-2)}`;
};
